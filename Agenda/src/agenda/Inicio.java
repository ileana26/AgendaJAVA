    private void jtContactoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtContactoMouseClicked
       
        //conectar con la sig interfaz
        Info nuevo = new Info();
        nuevo.setVisible(true);
        this.dispose();
        
        PreparedStatement ps=null;
        ResultSet rs=null;
        
        try{
            
        conexion con = new conexion ();
        Connection conn = con.conectar();
        
        int fila = jtContacto.getSelectedRow();
        String idContacto = jtContacto.getValueAt(fila, 0).toString();
        
        ps = conn.prepareStatement("select idContacto, numero, correo, fechaNa, sexo, domicilio from contacto where nombre=?");
        ps.setString(1, idContacto);
        rs = ps.executeQuery();
        
        while(rs.next())
        {
            String idd = rs.getString("idContacto");
            String num = rs.getString("numero");
            String correoo = rs.getString("correo");
            String fecha = rs.getString("fechaNa");
            String sexoo = rs.getString("sexo");
            String dom = rs.getString("domicilio");
            
            nuevo.id = idd;
            nuevo.numero = num;
            nuevo.correo = correoo;
            nuevo.cumple = fecha;
            nuevo.sexo = sexoo;
            nuevo.domi = dom;
            
        }
        
        }catch (SQLException e)
        {
            System.out.println(e.toString());
        }
        
    }//GEN-LAST:event_jtContactoMouseClicked

